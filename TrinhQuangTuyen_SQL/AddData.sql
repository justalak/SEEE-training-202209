--ADD CLASS DATA
DECLARE @numCLASS INT;
SET @numCLASS =0;
WHILE (@numCLASS<50) 
BEGIN
DECLARE @StartDateCLASS DATE;
DECLARE @EndDateCLASS  DATE;
DECLARE @starttime DATE;
DECLARE @endtime DATE;
SET @StartDateCLASS = '01/01/2016';
SET @EndDateCLASS   = '12/31/2021';
 SET @starttime = DATEADD(DAY, RAND(CHECKSUM(NEWID()))*(1+DATEDIFF(DAY, @StartDateCLASS, @EndDateCLASS)),@StartDateCLASS) ;
 set @endtime = DATEADD(MONTH, 6, @starttime) ;
DECLARE @teacher_id INT;
SET @teacher_id = FLOOR(100*RAND());
INSERT INTO class (CLASS_ID,CLASS_NAME,TEACHER_ID,START_TIME,END_TIME) 
VALUES (@numCLASS,CONCAT('CLASS_NAME_',@numCLASS),@teacher_id,@starttime,@endtime);
SET @numCLASS = @numCLASS+1;
END
