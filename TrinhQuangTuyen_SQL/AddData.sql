--ADD STUDENT DATA
DECLARE @numSTUDENT INT;
SET @numSTUDENT=0;
WHILE (@numSTUDENT <1000) 
BEGIN
DECLARE @gen VARCHAR(45);
SET @gen =CASE FLOOR(RAND()*2) WHEN 1 THEN  'MALE' ELSE 'FEMALE' END;
DECLARE @StartDate DATE;
DECLARE @EndDate  DATE;
DECLARE @birthday DATE;
SET @StartDate = '01/01/60';
SET @EndDate   = '12/31/1995';
 SET @birthday = DATEADD(DAY, RAND(CHECKSUM(NEWID()))*(1+DATEDIFF(DAY, @StartDate, @EndDate)),@StartDate) ;
DECLARE @score INT;
SET @score = FLOOR(RAND()*10);
DECLARE @class_id INT;
SET @class_id = FLOOR(RAND()*50);
INSERT INTO student (STUDENT_ID,STUDENT_NAME,GENDER,BIRTHDAY_STUDENT,EMAIL_STUDENT,PHONENUMBER_STUDENT,SCORE,CLASS_ID)
VALUES(@numSTUDENT,CONCAT('STUDENT_NAME_',@numSTUDENT),@gen, @birthday,CONCAT('EMAIL_STUDENT_',@numSTUDENT),CONCAT('PHONE_STUDENT_',@numSTUDENT),@score,@class_id);
SET @numSTUDENT= @numSTUDENT+1;
END
