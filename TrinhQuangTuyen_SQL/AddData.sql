--ADD TEACHER DATA
DECLARE @numTEACHER INT;
SET @numTEACHER=0;
WHILE (@numTEACHER<100)
BEGIN
DECLARE @StartDateTEACHER DATE;
DECLARE @EndDateTEACHER  DATE;
DECLARE @birthdayTEACHER DATE;
SET @StartDateTEACHER = '01/01/60';
SET @EndDateTEACHER   = '12/31/1995';
SET @birthdayTEACHER = DATEADD(DAY, RAND(CHECKSUM(NEWID()))*(1+DATEDIFF(DAY, @StartDateTEACHER, @EndDateTEACHER)),@StartDateTEACHER) ;
DECLARE @stayyear INT;
SET @stayyear = FLOOR(RAND()*(2010-1990)+1990);
INSERT INTO teacher (TEACHER_ID,TEACHER_NAME,BIRTHDAY_TEACHER,EMAIL_TEACHER,PHONENUMBER_TEACHER,STARTYEAR) 
VALUES (@numTEACHER,CONCAT('TEACHER_NAME_',@numTEACHER),@birthdayTEACHER,CONCAT('EMAIL_TEACHER_',@numTEACHER),CONCAT('PHONE_TEACHER_',@numTEACHER),@stayyear);
SET @numTEACHER = @numTEACHER+1;
END
